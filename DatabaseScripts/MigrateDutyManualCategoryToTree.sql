    FOREIGN KEY (CATEGORY_ID) REFERENCES MODUWMNL_CATEGORY (CATEGORY_ID);
ALTER TABLE MODUWMNL ADD KEY IX_MODUWMNL_01_NEW (USE_YN, LST_YN, CATEGORY_ID);
ALTER TABLE MODUWMNL DROP COLUMN CATEGORY;

DROP TABLE TMP_MODUWMNL_CATEGORY_PATH;
